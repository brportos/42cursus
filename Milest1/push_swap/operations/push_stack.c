#include "../push_swap.h"

void	pa(t_stack **a, t_stack **b, t_stats *st)
{
	t_stack	*tmp;

	if (!b || !(*b))
		return ;
	tmp = *b;
	*b = (*b)->next;
	tmp->next = *a;
	*a = tmp;
	write(1, "pa\n", 3);
	if (st)
	{
		st->pa++;
		st->total++;
	}
}

void	pb(t_stack **a, t_stack **b, t_stats *st)
{
	t_stack	*tmp;

	if (!a || !*a)
		return ;
	tmp = *a;
	*a = (*a)->next;
	tmp->next = *b;
	*b = tmp;
	write(1, "pb\n", 3);
	if (st)
	{
		st->pb++;
		st->total++;
	}
}
