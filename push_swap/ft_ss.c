#include "push_swap.h"

void	ft_ss(t_stack **a, t_stack **b)
{
	if (!a || !b || !(*a) || !(*b) || !(*a)->next || !(*b)->next)
		return ;
	ft_swap(a);
	ft_swap(b);
	write(1, "ss\n", 3);
}
